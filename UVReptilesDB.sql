-- All snake data with foreign key data from producers and buyers tables.

SELECT SNAKES.SNAKEID,
  SNAKES.SNAKENAME,
  SNAKES.SNAKEHATCHDATE,
  SNAKES.SNAKEGENDER,
  SNAKES.SNAKEDAM,
  SNAKES.SNAKESIRE,
  SNAKES.SNAKESTATUS,
  PRODUCERS.PRODUCERCOMPANYNAME,
  PRODUCERS.PRODUCERFIRSTNAME || ' ' ||  PRODUCERS.PRODUCERLASTNAME AS PRODUCER_NAME,
  BUYERS.BUYERCOMPANYNAME,
  BUYERS.BUYERFIRSTNAME || ' ' || BUYERS.BUYERLASTNAME AS BUYER_NAME
FROM SNAKES
INNER JOIN PRODUCERS
ON PRODUCERS.PRODUCERID = SNAKES.PRODUCERID
INNER JOIN BUYERS
ON BUYERS.BUYERID = SNAKES.BUYERID;

