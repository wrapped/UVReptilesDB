DROP TABLE snakes;
DROP TABLE buyers;
DROP TABLE producers;

CREATE TABLE buyers (
	buyerid NUMBER,
	buyercompanyname VARCHAR2(20 BYTE),
	buyerfirstname VARCHAR2(20 BYTE),
	buyerlastname VARCHAR2(20 BYTE),
	buyerstreetaddress VARCHAR2(50 BYTE),
	buyercity VARCHAR2(50 BYTE),
	buyerstate VARCHAR2(2 BYTE),
	buyerzipcode NUMBER,
	buyeremail VARCHAR2(50 BYTE),
	buyerphone NUMBER,
	CONSTRAINT buyer_pk PRIMARY KEY (buyerid)
);


CREATE TABLE producers (
	producerid NUMBER,
	producercompanyname VARCHAR2(50 BYTE),
	producerfirstname VARCHAR2(20 BYTE) NOT NULL,
	producerlastname VARCHAR2(20 BYTE),
	producerstreetaddress VARCHAR2(50 BYTE),
	producercity VARCHAR2(50 BYTE),
	producerstate VARCHAR2(2 BYTE),
	producerzipcode NUMBER(5,0),
	produceremail VARCHAR2(50 BYTE),
	producerphone NUMBER,
	CONSTRAINT producer_pk PRIMARY KEY (producerid)
);

CREATE TABLE snakes (
	snakeid VARCHAR2(20 BYTE),
	snakename VARCHAR2(20 BYTE),
	snakehatchdate DATE,
	snakegender VARCHAR2(20 BYTE) NOT NULL,
	snakesire VARCHAR2(20 BYTE),
	snakedam VARCHAR2(20 BYTE),
	producerid NUMBER,
	buyerid NUMBER,
	snakestatus VARCHAR2(20 BYTE) NOT NULL,
	CONSTRAINT snake_pk PRIMARY KEY (snakeid),
	CONSTRAINT producerid_fk FOREIGN KEY (producerid) REFERENCES producers (producerid),
	CONSTRAINT buyerid_fk FOREIGN KEY (buyerid) REFERENCES buyers (buyerid)
);

